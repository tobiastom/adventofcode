struct Point: Hashable {
    var y = 0
    var x = 0
}

struct Mountain {
    var trees = [Point]()
    var size = 0...0
    var width = 0

    init(input: String) {
        for (y, row) in input.split(whereSeparator: \.isNewline).enumerated() {
            width = row.count
            for (x, character) in row.enumerated() where character == "#" {
                trees.append(Point(y: y, x: x))
                size = 0...y
            }
        }
    }

    func count(right: Int, down: Int) -> Int {
        var position: Point = Point()
        var found = [Point]()

        for _ in size {
            position = Point(
                y: position.y + down,
                x: (position.x + right) % width
            )

            guard trees.contains(position) else {
                continue
            }

            found.append(position)
        }

        return found.count
    }
}

var mountain = Mountain(input: input)

print(mountain.count(right: 3, down: 1))

var slopes = [
    (1, 1),
    (3, 1),
    (5, 1),
    (7, 1),
    (1, 2)
];

print(slopes.map { slope -> Int in
    let (right, down) = slope
    return mountain.count(right: right, down: down)
}.reduce(1, *))
